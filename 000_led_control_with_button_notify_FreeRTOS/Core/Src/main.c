/* USER CODE BEGIN 4 */
void delay_FreeRTOS(uint32_t delay_ms){
	// MS TO TICK
	uint32_t current_tick = xTaskGetTickCount();
	uint32_t delay_tick = pdMS_TO_TICKS(delay_ms);   //delay_ms*configTICK_RATE_HZ/1000;
	// wait for tick
	while(xTaskGetTickCount()<(current_tick+delay_tick));
}
